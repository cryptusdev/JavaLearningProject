package com.yollacademy.lessons.replit;

import java.util.Scanner;

public class Replit110 {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int n = scan.nextInt();
        int j = 0;
        for (j = 0; j < n; j++) {
            System.out.print("*");
        }
    }
}

